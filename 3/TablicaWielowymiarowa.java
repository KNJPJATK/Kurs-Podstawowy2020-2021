public class TablicaWielowymiarowa {

	public static void main(String[] args) {
		int[][] tablicaWielowymiarowa = {
			{7, 8, 9},
			{4, 5, 6},
			{1, 2, 3},
			{0},
		};
		System.out.println(tablicaWielowymiarowa[0][0]);
		System.out.println(tablicaWielowymiarowa[0][1]);
		System.out.println(tablicaWielowymiarowa[1][0]);
		System.out.println(tablicaWielowymiarowa[1][1]);
	}

}
