public class TablicaWielowymiarowaLength {

	public static void main(String[] args) {
		int[][] tablicaWielowymiarowa = {
			{7, 8, 9},
			{4, 5, 6},
			{1, 2, 3},
			{0},
		};
		System.out.println(tablicaWielowymiarowa.length);
		System.out.println(tablicaWielowymiarowa[0].length);
		System.out.println(tablicaWielowymiarowa[1].length);
		System.out.println(tablicaWielowymiarowa[2].length);
		System.out.println(tablicaWielowymiarowa[3].length);
	}

}
