public class Literaly {

	public static void main(String[] args) {
		long literalL = 7291l;
		float literalF = 9.89f;
		int literal0x = 0xABBA;
		int literal0b = 0b1010;
		int literal0 = 0777;
		int literal_ = 123_456_789;

		System.out.println(literalL);
		System.out.println(literalF);
		System.out.println(literal0x);
		System.out.println(literal0b);
		System.out.println(literal0);
		System.out.println(literal_);
	}

}
