public class RozneTypy {

	public static void main(String[] args) {
		// Zmienne typu całkowitego
		byte zmiennaTypuByte = 97;
		char zmiennaTypuChar = 'a';
		short zmiennaTypuShort = 97;
		long zmiennaTypuLong = 97;
		int zmiennaTypuInt = 97;

		System.out.println(zmiennaTypuByte);
		System.out.println(zmiennaTypuChar);
		System.out.println(zmiennaTypuShort);
		System.out.println(zmiennaTypuLong);
		System.out.println(zmiennaTypuInt);

		// Zmienne typu zmiennoprzecinkowego
		double zmiennaTypuDouble = 3.14;
		float zmiennaTypuFloat = 3.14F;

		System.out.println(zmiennaTypuDouble);
		System.out.println(zmiennaTypuFloat);

		// Zmienne typu logicznego
		boolean prawda = true;
		boolean falsz = false;

		System.out.println(prawda);
		System.out.println(falsz);

		// Zmienne typu tekstowego
		String zmiennaTypuString = "Łańcuch tekstowy";
		
		System.out.println(zmiennaTypuString);
	}

}